def frequenciesCounter():
    frequencies={}
    for value in codon_map.values():
        #initialize for first time
        if value not in frequencies:
            frequencies[value]=0
        #increase the occurancess counter
        frequencies[value]+=1
    return frequencies

def possibleSequnecesCounter(string):
    frequencies=frequenciesCounter()
    #initalize the counter at first we have 3 for stop
    result=frequencies['Stop']
    for s in string:
        result*=frequencies[s]
    return result

